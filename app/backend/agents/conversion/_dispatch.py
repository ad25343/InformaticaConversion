def _build_approved_fixes_section(accepted_fixes: list[str] | None) -> str:
    """Build the reviewer-approved fixes prompt section."""
    if not accepted_fixes:
        return ""
    numbered = "\n".join(f"{i+1}. {fix}" for i, fix in enumerate(accepted_fixes))
    return (
        f"\n## Reviewer-Approved Fixes — Apply These Exactly\n"
        f"The human reviewer has reviewed the verification flags and approved the following "
        f"specific fixes. You MUST apply each one precisely as described — do not skip, "
        f"paraphrase, or generalise:\n\n{numbered}\n\n"
    )


def _build_security_fix_section(security_findings: list[dict] | None) -> str:
    """Build the security findings prompt section."""
    if not security_findings:
        return ""
    finding_lines = [
        _format_security_finding(i, f)
        for i, f in enumerate(security_findings, 1)
    ]
    return (
        "\n## Security Findings — You MUST Fix All of These\n"
        "A human security reviewer rejected the previous code generation and requested fixes. "
        "The EXACT offending code snippets are shown for each finding — you must NOT reproduce "
        "these patterns anywhere in the regenerated code. For each finding, apply the stated "
        "fix or an equivalent secure alternative:\n\n"
        + "\n\n".join(finding_lines)
        + "\n\n"
    )


def _finding_fix_text(fix: str) -> str:
    """Return fix guidance or a default instruction."""
    return fix or "Do not reproduce this pattern in the regenerated code."


def _finding_snippet(location: str, line_ref: str, code: str) -> str:
    """Return a code snippet block if code is non-empty, else empty string."""
    if not code:
        return ""
    return f"\n   Offending code ({location}{line_ref}):\n   ```\n   {code}\n   ```"


def _format_security_finding(idx: int, f: dict) -> str:
    """Format a single security finding entry."""
    sev      = f.get("severity", "UNKNOWN")
    ftype    = f.get("test_name") or f.get("finding_type", "security issue")
    location = f.get("filename") or f.get("location", "")
    desc     = f.get("text") or f.get("description", "")
    fix      = f.get("remediation", "")
    code     = f.get("code", "").strip()
    line     = f.get("line")
    line_ref = f" line {line}" if line else ""
    snippet  = _finding_snippet(location, line_ref, code)
    return (
        f"{idx}. [{sev}] {ftype} — {location}{line_ref}\n"
        f"   Issue: {desc}{snippet}\n"
        f"   Fix required: {_finding_fix_text(fix)}"
    )


_BEGIN_PATTERN = re.compile(r"<<<BEGIN_FILE:\s*(.+?)>>>", re.IGNORECASE)
_END_MARKER    = "<<<END_FILE>>>"
_NOTES_BEGIN   = "<<<NOTES>>>"
_NOTES_END     = "<<<END_NOTES>>>"


def _extract_files_from_delimiters(raw: str) -> tuple[dict[str, str], list[str]]:
    """Extract file contents from delimiter-format Claude output."""
    files: dict[str, str] = {}
    notes: list[str] = []
    pos = 0
    while pos < len(raw):
        m = _BEGIN_PATTERN.search(raw, pos)
        if not m:
            break
        fname = m.group(1).strip()
        content_start = m.end()
        end_pos = raw.find(_END_MARKER, content_start)
        if end_pos == -1:
            files[fname] = raw[content_start:].lstrip("\n")
            notes.append(f"File '{fname}' appears truncated (no END_FILE marker found).")
            break
        files[fname] = raw[content_start:end_pos].lstrip("\n").rstrip("\n")
        pos = end_pos + len(_END_MARKER)
    return files, notes


def _extract_notes_from_raw(raw: str) -> list[str]:
    """Extract the NOTES section from Claude delimiter output."""
    n_start = raw.find(_NOTES_BEGIN)
    if n_start == -1:
        return []
    n_end = raw.find(_NOTES_END, n_start)
    notes_text = raw[n_start + len(_NOTES_BEGIN): n_end if n_end != -1 else len(raw)].strip()
    return [ln.strip("- •").strip() for ln in notes_text.splitlines() if ln.strip("- •").strip()]


def _parse_delimiter_output(raw: str) -> tuple[dict[str, str], list[str], bool]:
    """
    Parse Claude's delimiter-format output into files dict and notes list.
    Returns (files, notes, parsed_ok).
    """
    files, file_notes = _extract_files_from_delimiters(raw)
    notes = file_notes + _extract_notes_from_raw(raw)
    return files, notes, bool(files)


def _handle_truncation(files: dict[str, str], notes: list[str], stop_reason: str, stack, raw: str) -> None:
    """Mutate notes in-place: add truncation/fallback warnings."""
    if not files:
        files[f"converted_{stack.value.lower()}_raw.txt"] = raw
        notes.append(
            "Conversion output did not use the expected file delimiter format. "
            "Raw response saved. Re-running the job should resolve this."
        )
        return
    if stop_reason == "max_tokens":
        notes.append(
            "Response reached the token limit — one or more files may be truncated. "
            "Review the last file carefully. If incomplete, re-run the job."
        )


def _inject_yaml_artifacts(
    files: dict[str, str],
    notes: list[str],
    session_parse_report: Optional[SessionParseReport],
) -> None:
    """Inject YAML config artifacts into files if session data is available."""
    if not (session_parse_report and session_parse_report.session_config):
        return
    try:
        yaml_files = _build_yaml_artifacts(session_parse_report)
        files.update(yaml_files)
        notes.append(
            f"v1.1: Generated {len(yaml_files)} config artifact(s): "
            + ", ".join(yaml_files.keys())
        )
    except Exception as e:
        notes.append(f"v1.1: YAML artifact generation failed (non-blocking): {e}")


def _inject_dbt_artifacts(
    files: dict[str, str],
    notes: list[str],
    stack,
    stack_assignment: StackAssignment,
    session_parse_report: Optional[SessionParseReport],
) -> None:
    """Inject dbt runtime artifacts if stack is DBT."""
    if stack != TargetStack.DBT:
        return
    try:
        dbt_rt = build_dbt_runtime_artifacts(stack_assignment, session_parse_report)
        files.update(dbt_rt)
        notes.append(
            f"v2.7: Generated dbt runtime artifacts: {', '.join(dbt_rt.keys())}. "
            "Set the environment variables in profiles.yml before running."
        )
    except Exception as e:
        notes.append(f"v2.7: dbt runtime artifact generation failed (non-blocking): {e}")


def _inject_etl_pattern_artifacts(
    files: dict[str, str],
    notes: list[str],
    stack_assignment: StackAssignment,
    complexity_report: Optional[ComplexityReport],
    graph: dict,
) -> None:
    """Inject etl_patterns config skeleton if a pattern was detected."""
    if not (complexity_report and complexity_report.suggested_pattern):
        return
    if complexity_report.pattern_confidence in (None, "NONE"):
        return
    try:
        mapping_slug = stack_assignment.mapping_name.lower().replace(" ", "_")
        pattern_yaml = _build_pattern_yaml_skeleton(
            pattern=complexity_report.suggested_pattern,
            mapping_name=stack_assignment.mapping_name,
            graph=graph,
        )
        config_key = f"config/{mapping_slug}.yaml"
        files[config_key] = pattern_yaml
        files["run.py"] = _build_etl_patterns_run_script(
            mapping_name=stack_assignment.mapping_name,
            pattern=complexity_report.suggested_pattern,
        )
        notes.append(
            f"v2.16.0 (etl_patterns): Pattern '{complexity_report.suggested_pattern}' "
            f"detected with {complexity_report.pattern_confidence} confidence. "
            f"Starter config emitted to '{config_key}'. "
            "Install etl_patterns (`pip install -e etl_patterns/`) and run: "
            f"python run.py {config_key}"
        )
    except Exception as e:
        notes.append(
            f"v2.16.0 (etl_patterns): config skeleton generation failed (non-blocking): {e}"
        )


def _norm_flags(verification_flags) -> list:
    """Normalise verification flags to plain dicts (model_dump if Pydantic, else as-is)."""
    return [f.model_dump() if hasattr(f, "model_dump") else f for f in (verification_flags or [])]


