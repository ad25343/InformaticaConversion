def _build_etl_patterns_run_script(mapping_name: str, pattern: str) -> str:
    """
    Return a minimal run.py that executes an etl_patterns config YAML.
    This gives the developer a ready-to-run entry point alongside the
    generated config skeleton.
    """
    slug = mapping_name.lower().replace(" ", "_")
    return (
        "#!/usr/bin/env python\n"
        '"""run.py — Execute an etl_patterns config YAML.\n\n'
        "Generated by the Informatica Conversion Tool (v2.16.0-phase5).\n\n"
        "Usage:\n"
        f"    python run.py config/{slug}.yaml\n\n"
        "Prerequisites:\n"
        "    pip install -e etl_patterns/    # install the pattern library\n"
        '"""\n'
        "from __future__ import annotations\n"
        "import sys\n"
        "import logging\n"
        "from etl_patterns import config_loader\n\n"
        'logging.basicConfig(level=logging.INFO, format="%(levelname)s %(name)s %(message)s")\n\n'
        'if __name__ == "__main__":\n'
        "    if len(sys.argv) < 2:\n"
        f'        print("Usage: python run.py <config_yaml_path>")\n'
        "        sys.exit(1)\n"
        "    config_path = sys.argv[1]\n"
        "    result = config_loader.run(config_path)\n"
        '    print(f"Result: {result}")\n'
        '    sys.exit(0 if result.get("status") == "success" else 1)\n'
    )


