def _append_parameters_block(lines: list[str], spr: SessionParseReport) -> None:
    """Append the resolved-parameters block to lines (mutates in place)."""
    if not spr.parameters:
        return
    lines += ["", "# Resolved parameters (from parameter file)", "parameters:"]
    for p in spr.parameters:
        safe_val = str(p.value).replace('"', '\\"')
        lines.append(f'  {p.name}: "{safe_val}"  # scope: {p.scope}')


def _append_unresolved_vars_block(lines: list[str], spr: SessionParseReport) -> None:
    """Append the unresolved-variables block to lines (mutates in place)."""
    if not spr.unresolved_variables:
        return
    lines += ["", "# Unresolved variables — values must be supplied at runtime", "unresolved_variables:"]
    for v in spr.unresolved_variables:
        lines.append(f"  {v}: <fill_in>")


def _build_connections_yaml(spr: SessionParseReport) -> str:
    """Build connections.yaml content from session parse report."""
    sc = spr.session_config
    lines: list[str] = [
        "# connections.yaml — generated by Informatica Conversion Tool v1.1",
        f"# Session: {sc.session_name if sc else 'N/A'}",
        f"# Workflow: {sc.workflow_name if sc else 'N/A'}",
        "#",
        "# Fill in the <placeholders> before use.",
        "",
        "connections:",
    ]

    if sc and sc.connections:
        lines.extend(_build_connection_entries(sc.connections))
    else:
        lines.append("  # No connections extracted from Workflow XML")

    _append_parameters_block(lines, spr)
    _append_unresolved_vars_block(lines, spr)
    return "\n".join(lines) + "\n"


def _build_single_connection_lines(conn) -> list[str]:
    """Build YAML lines for a single connection object."""
    lines = [f"  {conn.transformation_name}:", f"    role: {conn.role}"]
    optional_str_fields = [
        ("connection_name", conn.connection_name),
        ("connection_type", conn.connection_type),
        ("file_name", conn.file_name),
        ("file_dir", conn.file_dir),
    ]
    for field_name, value in optional_str_fields:
        if value:
            lines.append(f"    {field_name}: {value}")
    if conn.connection_type and conn.connection_type.upper() in ("RELATIONAL", "ODBC"):
        lines += ["    jdbc_url: <fill_in>", "    username: <fill_in>", "    password: <fill_in>"]
    return lines


def _build_connection_entries(connections: list) -> list[str]:
    """Build YAML lines for each connection (deduped by connection_key)."""
    lines: list[str] = []
    seen: set[str] = set()
    for conn in connections:
        conn_key = conn.connection_name or conn.transformation_name
        if conn_key in seen:
            continue
        seen.add(conn_key)
        lines.extend(_build_single_connection_lines(conn))
    return lines


def _build_runtime_config_yaml(spr: SessionParseReport) -> str:
    """Build runtime_config.yaml content from session parse report."""
    sc = spr.session_config
    lines: list[str] = [
        "# runtime_config.yaml — generated by Informatica Conversion Tool v1.1",
        f"# Session: {sc.session_name if sc else 'N/A'}",
        f"# Workflow: {sc.workflow_name if sc else 'N/A'}",
        "",
        "session:",
    ]

    if sc:
        lines.extend(_build_session_config_lines(sc))
    else:
        lines.append("  # No session config extracted from Workflow XML")

    return "\n".join(lines) + "\n"


def _append_sql_block(lines: list[str], label: str, sql: str) -> None:
    """Append a multi-line SQL block to lines (mutates in place)."""
    if not sql:
        return
    lines += ["", f"  {label}: |"]
    lines.extend(f"    {ln}" for ln in sql.splitlines())


def _build_session_config_lines(sc) -> list[str]:
    """Build YAML lines for session config fields."""
    lines = [
        f"  name: {sc.session_name}",
        f"  mapping: {sc.mapping_name}",
        f"  workflow: {sc.workflow_name}",
    ]
    # Scalar optionals
    scalar_fields = [
        ("commit_interval", sc.commit_interval),
        ("error_threshold", sc.error_threshold),
        ("reject_filename", sc.reject_filename),
        ("reject_filedir", sc.reject_filedir),
    ]
    for field_name, value in scalar_fields:
        if value is not None:
            lines.append(f"  {field_name}: {value}")
    _append_sql_block(lines, "pre_session_sql", sc.pre_session_sql or "")
    _append_sql_block(lines, "post_session_sql", sc.post_session_sql or "")
    return lines


def _build_yaml_artifacts(spr: SessionParseReport) -> dict[str, str]:
    """
    Generate connections.yaml and runtime_config.yaml from Step 0 session data.
    Returns a dict of {filename: yaml_content}.
    """
    return {
        "config/connections.yaml": _build_connections_yaml(spr),
        "config/runtime_config.yaml": _build_runtime_config_yaml(spr),
    }


